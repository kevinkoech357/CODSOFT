#!/usr/bin/env python3

"""
Contains all defined operands for the simple calculator.
"""


def add(arg1, arg2):
    """
    Perform addition.
    """
    return arg1 + arg2


def subtract(arg1, arg2):
    """
    Perform subtraction.
    """
    return arg1 - arg2


def multiply(arg1, arg2):
    """
    Perform multiplication.
    """
    return arg1 * arg2


def divide(arg1, arg2):
    """
    Perform division.
    """
    return arg1 / arg2


def find_remainder(arg1, arg2):
    """
    Find remainder.
    """
    return arg1 % arg2
